const initCardHoverSystem = () => {
  const cards = Array.from(document.querySelectorAll(interactiveCardSelector));
  const resetDelay = 320;
  const nearRadius = 420;
  if (!cards.length) return;

  const clamp = (value, min, max) => Math.min(max, Math.max(min, value));

  const getSectionCards = (card) => {
    const section = card.closest(".section");
    if (!section) return [card];
    return Array.from(section.querySelectorAll(interactiveCardSelector));
  };

  const updateSpotPosition = (card, clientX, clientY) => {
    const rect = card.getBoundingClientRect();
    const relativeX = ((clientX - rect.left) / rect.width) * 100;
    const relativeY = ((clientY - rect.top) / rect.height) * 100;
    card.style.setProperty("--spot-x", `${clamp(relativeX, 0, 100).toFixed(2)}%`);
    card.style.setProperty("--spot-y", `${clamp(relativeY, 0, 100).toFixed(2)}%`);
  };

  const updateDepthState = (card, clientX, clientY) => {
    if (prefersReducedMotion) {
      return;
    }

    const rect = card.getBoundingClientRect();
    const x = clientX - rect.left;
    const y = clientY - rect.top;
    const centerX = rect.width / 2;
    const centerY = rect.height / 2;
    const offsetX = (x - centerX) * 0.012;
    const offsetY = (y - centerY) * 0.01 - 2;
    const rotateY = ((x - centerX) / rect.width) * 1.15;
    const rotateX = ((centerY - y) / rect.height) * 1.05;

    card.style.setProperty("--hover-shift-x", `${offsetX.toFixed(2)}px`);
    card.style.setProperty("--hover-shift-y", `${offsetY.toFixed(2)}px`);
    card.style.setProperty("--hover-rotate-x", `${rotateX.toFixed(2)}deg`);
    card.style.setProperty("--hover-rotate-y", `${rotateY.toFixed(2)}deg`);
  };

  const applyNearbyResponse = (sourceCard, clientX, clientY) => {
    getSectionCards(sourceCard).forEach((card) => {
      updateSpotPosition(card, clientX, clientY);

      if (card === sourceCard) {
        card.style.setProperty("--card-near", "1");
        card.classList.remove("is-near");
        return;
      }

      const rect = card.getBoundingClientRect();
      const dx = clientX < rect.left ? rect.left - clientX : clientX > rect.right ? clientX - rect.right : 0;
      const dy = clientY < rect.top ? rect.top - clientY : clientY > rect.bottom ? clientY - rect.bottom : 0;
      const distance = Math.hypot(dx, dy);
      const strength = Math.max(0, 1 - distance / nearRadius);

      card.style.setProperty("--card-near", strength.toFixed(3));
      card.classList.toggle("is-near", strength > 0.04);
    });
  };

  const clearSectionResponse = (sourceCard) => {
    getSectionCards(sourceCard).forEach((card) => {
      card.classList.remove("is-near");
      card.style.setProperty("--card-near", "0");
      if (card !== sourceCard) {
        card.style.setProperty("--hover-shift-x", "0px");
        card.style.setProperty("--hover-shift-y", "0px");
        card.style.setProperty("--hover-rotate-x", "0deg");
        card.style.setProperty("--hover-rotate-y", "0deg");
      }
    });
  };

  cards.forEach((card) => {
    card.classList.add("interactive-card");
    card.style.setProperty("--spot-x", "50%");
    card.style.setProperty("--spot-y", "50%");
    card.style.setProperty("--card-near", "0");
    card.style.setProperty("--hover-shift-x", "0px");
    card.style.setProperty("--hover-shift-y", "0px");
    card.style.setProperty("--hover-rotate-x", "0deg");
    card.style.setProperty("--hover-rotate-y", "0deg");

    if (!card.querySelector(".card-spotlight")) {
      ["card-spotlight", "card-border-glow", "card-hover-sheen"].forEach((className) => {
        const layer = document.createElement("span");
        layer.className = className;
        layer.setAttribute("aria-hidden", "true");
        card.appendChild(layer);
      });
    }
  });

  if (!hoverEnabledQuery.matches) {
    return;
  }

  cards.forEach((card) => {
    const clearResetTimer = () => {
      if (card._hoverResetTimer) {
        window.clearTimeout(card._hoverResetTimer);
        card._hoverResetTimer = null;
      }
    };

    const resetCard = () => {
      card.classList.remove("is-hovered");
      card.style.setProperty("--hover-shift-x", "0px");
      card.style.setProperty("--hover-shift-y", "0px");
      card.style.setProperty("--hover-rotate-x", "0deg");
      card.style.setProperty("--hover-rotate-y", "0deg");
      clearSectionResponse(card);
      clearResetTimer();
      card._hoverResetTimer = window.setTimeout(() => {
        if (!card.matches(":hover") && !card.classList.contains("is-hovered")) {
          card.style.setProperty("--spot-x", "50%");
          card.style.setProperty("--spot-y", "50%");
        }
      }, resetDelay);
    };

    const handlePointerEnter = (event) => {
      clearResetTimer();
      card.classList.add("is-hovered");
      applyNearbyResponse(card, event.clientX, event.clientY);
      updateDepthState(card, event.clientX, event.clientY);
    };

    const handlePointerMove = (event) => {
      clearResetTimer();
      applyNearbyResponse(card, event.clientX, event.clientY);
      updateDepthState(card, event.clientX, event.clientY);
    };

    card.addEventListener("pointerenter", handlePointerEnter);
    card.addEventListener("pointermove", handlePointerMove);
    card.addEventListener("pointerleave", resetCard);
    card.addEventListener("pointercancel", resetCard);
  });
};
