const applyLanguage = (lang) => {
  const dictionary = translations[lang];
  document.querySelectorAll("[data-i18n]").forEach((node) => {
    const key = node.dataset.i18n;
    if (dictionary[key]) node.textContent = dictionary[key];
  });
  html.lang = lang;
  pills.forEach((pill, index) => {
    const active = (lang === "en" && index === 0) || (lang === "fr" && index === 1);
    pill.dataset.active = active ? "true" : "false";
  });
  localStorage.setItem("portfolio-language", lang);
  window.dispatchEvent(new CustomEvent("portfolio:languagechange", { detail: { lang } }));
  if (typeof window.updateRobotGuideLanguage === "function") {
    window.updateRobotGuideLanguage(lang);
  }
};

const closeMenu = () => {
  siteNav?.classList.remove("is-open");
  menuToggle?.setAttribute("aria-expanded", "false");
};

const syncResponsiveMenuState = (query = collapsedNavQuery) => {
  if (!query.matches) {
    closeMenu();
    return;
  }

  menuToggle?.setAttribute("aria-expanded", siteNav?.classList.contains("is-open") ? "true" : "false");
};

const setActiveNav = (id) => {
  const previousActiveId = currentNavSectionId;
  currentNavSectionId = id;

  navLinks.forEach((link) => {
    const isActive = link.getAttribute("href") === `#${id}`;
    link.classList.toggle("is-active", isActive);
    if (isActive) {
      link.setAttribute("aria-current", "page");
    } else {
      link.removeAttribute("aria-current");
    }
  });

  if (ROBOT_DEBUG && previousActiveId !== id) {
    console.debug("[robot-debug] ScrollTrigger active nav section", {
      previous: previousActiveId,
      current: id,
      scrollY: Math.round(window.scrollY || 0),
      viewport: {
        width: window.innerWidth || document.documentElement.clientWidth || 0,
        height: window.innerHeight || document.documentElement.clientHeight || 0,
        category: getViewportCategory()
      }
    });
  }

  if (ROBOT_DEBUG && typeof window.queueRobotGuideDebugRefresh === "function") {
    window.queueRobotGuideDebugRefresh("nav-change", true);
  }
};

